// Extension for easy access
extension TranslationExtension on BuildContext {
  String l10n(String key) {
    return TranslationManager.localizedValues[read<TranslationManager>().currentLocale.languageCode]?[key] ?? key;
  }
}

// Global accessor helper
String getT(BuildContext context, String key) {
  final provider = Provider.of<TranslationManager>(context);
  return TranslationManager.localizedValues[provider.currentLocale.languageCode]?[key] ?? key;
}
