class TranslationManager extends ChangeNotifier {
  static const String _langKey = 'selected_language';
  Locale _currentLocale = const Locale('ms'); // Default to Malay

  Locale get currentLocale => _currentLocale;
  bool get isMalay => _currentLocale.languageCode == 'ms';

  TranslationManager() {
    _loadLanguage();
  }

  Future<void> _loadLanguage() async {
    final prefs = await SharedPreferences.getInstance();
    final lang = prefs.getString(_langKey) ?? 'ms';
    _currentLocale = Locale(lang);
    notifyListeners();
  }

  Future<void> setLanguage(String langCode) async {
    _currentLocale = Locale(langCode);
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_langKey, langCode);
    notifyListeners();
  }

  void toggleLanguage() {
    if (isMalay) {
      setLanguage('en');
    } else {
      setLanguage('ms');
    }
  }

